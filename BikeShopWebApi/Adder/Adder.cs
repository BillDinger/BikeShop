namespace BikeShopWebApi.Adder
{
    /// <summary>
    /// A very simple class used to demonstrate the absolute basics of unit testing.
    /// </summary>
    public class Adder
    {
        public long Add(int x, int z)
        {
            return x + z;
        }

        public long Multiply(int x, int z)
        {
            return x * z;
        }
    }
}